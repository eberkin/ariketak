public class Kontaktua {
    public String izena;
    public int telefonoa;
    public void setIzena(String izena) {
        this.izena = izena;
    }
    public void setTelefonoa(int telefonoa) {
        this.telefonoa = telefonoa;
    }
    public String getIzena() {
        return izena;
    }
    public int getTelefonoa() {
        return telefonoa;
    }
    

}
